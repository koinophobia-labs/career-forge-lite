#!/usr/bin/env node
/**
 * Fingerprint the code whose behaviour an operational certification vouches for.
 *
 * Certification proves "this journey works". If any of the files that implement
 * the journey change, the proof no longer describes the running system — so the
 * hash is baked into the build and compared at runtime. Editing the webhook and
 * redeploying silently re-closes checkout, which is the correct outcome.
 *
 *   node scripts/compute-surface-hash.mjs          # write the constant
 *   node scripts/compute-surface-hash.mjs --check  # fail if stale
 */

import fs from "node:fs";
import path from "node:path";
import { createHash } from "node:crypto";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT = path.join(root, "src/lib/server/certified-surface-hash.ts");

// Kept in sync with CERTIFIED_SURFACE in src/lib/server/certification.ts.
const SURFACE = [
  "src/app/api/checkout/route.ts",
  "src/app/api/stripe-webhook/route.ts",
  "src/app/api/license/route.ts",
  "src/lib/server/stripe.ts",
  "src/lib/server/session-verification.ts",
  "src/lib/server/license-mint.ts",
  "src/lib/server/fulfillment-store.ts",
  "src/lib/server/fulfillment-readiness.ts",
  "src/lib/packages.ts",
];

const hash = createHash("sha256");
for (const rel of SURFACE) {
  const abs = path.join(root, rel);
  if (!fs.existsSync(abs)) {
    console.error(`certified surface file missing: ${rel}`);
    process.exit(1);
  }
  hash.update(rel);
  hash.update(fs.readFileSync(abs));
}
const digest = hash.digest("hex").slice(0, 32);

const contents = `// GENERATED by scripts/compute-surface-hash.mjs — do not edit by hand.
//
// Fingerprint of the checkout/webhook/license/storage/package code that an
// operational certification vouches for. If this changes, prior proof is void
// and checkout re-closes until the journey is certified again.

export const CERTIFIED_SURFACE_HASH = "${digest}";
`;

if (process.argv.includes("--check")) {
  const current = fs.existsSync(OUT) ? fs.readFileSync(OUT, "utf8") : "";
  if (current.trim() !== contents.trim()) {
    console.error(
      "Certified surface hash is stale.\n" +
        "Checkout-path code changed without regenerating the fingerprint.\n" +
        "Run: node scripts/compute-surface-hash.mjs"
    );
    process.exit(1);
  }
  console.log(`surface hash current: ${digest}`);
  process.exit(0);
}

fs.writeFileSync(OUT, contents);
console.log(`wrote ${path.relative(root, OUT)} (${digest})`);
